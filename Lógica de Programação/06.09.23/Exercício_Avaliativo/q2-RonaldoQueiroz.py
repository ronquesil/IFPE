# Solicitando um valor em metros do usuário
metros = float(input("Por favor, insira um valor em metros: "))

# Convertendo o valor de metros para milímetros (1 metro = 1000 milímetros)
milimetros = metros * 1000

# Imprimindo o resultado da conversão
print(f"O valor em milímetros é: {milimetros} mm")
