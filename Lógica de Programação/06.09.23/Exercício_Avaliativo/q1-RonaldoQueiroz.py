# Solicitando dois números inteiros do usuário
num1 = int(input("Por favor, insira o primeiro número inteiro: "))
num2 = int(input("Por favor, insira o segundo número inteiro: "))

# Calculando a soma dos dois números
soma = num1 + num2

# Imprimindo o resultado da soma
print("O resultado da soma dos dois números é:", soma)